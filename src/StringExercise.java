public class StringExercise {

    public static void main(String[] args) {

        String varOne = "We don't need no education\n";
        String varTwo = "We don't need no thought control";
        System.out.println(varOne + varTwo);

        String varThree = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(varThree);

        String varFour = "In windows, the main drive is usually C:\\";
        System.out.println(varFour);

        String varFive = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(varFive);


    }
}
